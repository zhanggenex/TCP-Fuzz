



public class InOut {
    
    
    private int id, cinout;
    private String inout;
   

    public InOut(int id, String inout, int cinout) {
        this.id = id;
        this.inout = inout;
        this.cinout = cinout;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getInout() {
        return inout;
    }

    public void setInout(String inout) {
        this.inout = inout;
    }

    public int getCinout() {
        return cinout;
    }

    public void setCinout(int cinout) {
        this.cinout = cinout;
    }
    
    
} 
